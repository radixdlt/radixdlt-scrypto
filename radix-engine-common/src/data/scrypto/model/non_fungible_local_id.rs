/// Represents an error when decoding non-fungible id.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum ParseNonFungibleLocalIdError {
    UnknownType,
    InvalidInteger,
    InvalidBytes,
    InvalidUUID,
    ContentValidationError(ContentValidationError),
}

#[cfg(not(feature = "alloc"))]
